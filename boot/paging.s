;	Set the last entry to point at the PML4T to create recursive mapping
	dq pml4t - HIGHER_HALF_OFFSET + 11b

pdpt_lower:
;   Identity map for the first 1GiB
    dq pdt - HIGHER_HALF_OFFSET + 11b

;    Set null for the rest of the entries
    times 511 dq 0

